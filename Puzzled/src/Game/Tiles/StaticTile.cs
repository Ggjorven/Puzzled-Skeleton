using System;
using System.Windows;
using System.Windows.Controls;
using System.Windows.Media;
using System.Windows.Media.TextFormatting;
using System.Windows.Shapes;

namespace Puzzled
{

    //////////////////////////////////////////////////////////////////////////////////
    // StaticTile
    //////////////////////////////////////////////////////////////////////////////////
    public class StaticTile
    {

        ////////////////////////////////////////////////////////////////////////////////////
        // Constructor & Destructor
        ////////////////////////////////////////////////////////////////////////////////////
        public StaticTile(Maths.Vector2 position, Maths.Vector2 size, ITexture texture)
        {
            m_Position = position;
            m_Size = size;
            m_Texture = texture;
        }
        ~StaticTile()
        {
        }

        ////////////////////////////////////////////////////////////////////////////////////
        // Methods
        ////////////////////////////////////////////////////////////////////////////////////
        public void RenderTo(Renderer renderer)
        {
            renderer.AddQuad(m_Position, m_Size, m_Texture);
        }

        ////////////////////////////////////////////////////////////////////////////////////
        // Variables
        ////////////////////////////////////////////////////////////////////////////////////
        private Maths.Vector2 m_Position;
        private Maths.Vector2 m_Size;
        private ITexture m_Texture;

        public Maths.Vector2 Position { get { return m_Position; } }
        public Maths.Vector2 Size { get { return m_Size; } }
    }

}